package Quizes;

public class Quiz5 {

    public static void main(String[] args) {

        int k = 1;
        int sum = 0;
        while (k != 4) {
            k++;
            sum += k;
        }

        // What are k and sum?
        System.out.println("k = " + k);
        System.out.println("sum = " + sum);

    }
}
